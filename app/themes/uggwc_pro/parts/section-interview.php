				<p><strong>GWC:</strong> Warum haben Sie sich für unsere Agentur entschieden, um Hilfe zu bekommen? </p>
				<p><strong>Benny:</strong> Ich schrieb meine Masterarbeit selbst. Ich habe den theoretischen Teil geschrieben und viel Zeit darauf verwendet. Recherche, Literatur sortieren, alles miteinander inhaltlich verbinden... und es waren noch 80 Seiten übrig, einschließlich des praktischen Teils. Das ging mir auf die Nerven. Ich arbeite noch daneben sehr viel. Das alles beeinflusste auch meine Arbeit. Ich beschloss, einen Ghostwriter zu finden. </p>
				<p><strong>GWC:</strong> Warum denn haben Sie unsere Agentur gewählt? </p>
				<p><strong>Benny:</strong> Na, sagen wir, was den Preis betrifft, so stehen Sie etwa in der Mitte. 120 Euro für eine Seite zu zahlen – das klingt komisch. Es gibt Agenturen, die bereit waren, für 30 Euro je Seite meine Arbeit zu erstellen. Das ist aber auch nicht seriös. Wie man sagt, Käse umsonst gibt es nur in der Mausefalle. Bei alledem haben Sie coole Boni angeboten, ich glaube also, dass ich noch Geld gespart habe. Der Autor war ständig mit mir in Kontakt, beantwortete meine Fragen und reagierte schnell, auch wenn alles über den Manager abgewickelt wurde und keine Zeit für eine telefonische Besprechung blieb. Außerdem habe ich eine lebenslange Garantie bekommen. Ich weiß nicht, ob ich sie brauche, aber nach meiner Erfahrung und dem Bereich, wo ich arbeite, bieten so was nur die Unternehmen an, die zu 200 % der Qualität ihrer Produkte sicher sind. So ungefähr mit kurzen Worten.</p>
				<p><strong>GWC:</strong> Wie war Ihre Zusammenarbeit mit uns?</p>
				<p><strong>Benny:</strong> Ich glaube, ich hab schon geantwortet. Es gab viel Arbeit, ich sprach mit dem Autor im Laufe der Arbeit und danach, obwohl es schon nicht so nötig war. Die Manager antworteten mir auch nachts, vielen Dank ihnen dafür. Das habe ich nicht erwartet. Alles hat mir gut gefallen. Ich habe auch coole Checklisten erhalten und benutze sie. Auch ein Buch hat man mir geschenkt. Dieser Service ist einer der besten, den ich gesehen habe. Wir arbeiten daran auch selbst in unserer Gesellschaft.  </p>
				<p><strong>GWC:</strong> Haben Sie Ratschläge für die Studenten, die auch nach einem Ghostwriter suchen?</p>
				<p><strong>Benny:</strong> Den Preis dieser Dienstleistungen zu beachten. Und was bieten die Gesellschaften für diesen Preis. Ich arbeite selber und verstehe etwas von Dienstleistungen.   </p>
